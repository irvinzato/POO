public class Rueda {
  private String brand;
  private int ring;
  private double width;

  public Rueda( String brand, int ring, double width ) {
    this.brand = brand;
    this.ring = ring;
    this.width = width;
  }

  public String getBrand() {
    return this.brand;
  }

  public int getRing() {
    return this.ring;
  }

  public double getWidth() {
    return this.width;
  }

}
